package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello Everyone");
        System.out.println("My name is Maria, I am from Chicago");

        System.out.println("==============");

        System.out.println("Hello Everyone\nMy name is Maria, I am from Chicago");

        System.out.println("=============");

        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

        System.out.println("================");

        System.out.println("/"); // prints single frontslash
        System.out.println("\\"); //prints single backslash

        System.out.println("================");

        System.out.println("My name is 'Maria'");
        System.out.println("My name is \'Maria\'");

        System.out.println("================");

        System.out.println("My favorite TV series is \"Friends\"");










    }
}

