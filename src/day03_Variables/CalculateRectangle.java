//1. write a program that can calculate the area & perimeter of any rectangle that has inter number


public class CalculateRectangle {

    public static void main(String[] args) {

        //     width :5, lenght : 20

        int width = 5;
        int lenght = 20;

        int area = width * lenght;
        // 5 * 20
int perimeter = width * 2 + lenght * 2;

        System.out.println("Area is:");
        System.out.println( area );

        System.out.println("\nPerimeter is:");
        System.out.println(perimeter);

    }


}
